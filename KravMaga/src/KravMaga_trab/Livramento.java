package KravMaga_trab;

public class Livramento {
	private int nivel;
	private String base;
	private MaoLivre maoLivre;
	private ArmaFogo armaFogo;
	private Faca faca;
	private ArmaBranca armaBranca;

	
	public MaoLivre getMaoLivre() {
		return maoLivre;
	}
	public void setMaoLivre(MaoLivre maoLivre) {
		this.maoLivre = maoLivre;
	}
	public ArmaFogo getArmaFogo() {
		return armaFogo;
	}
	public void setArmaFogo(ArmaFogo armaFogo) {
		this.armaFogo = armaFogo;
	}
	public Faca getFaca() {
		return faca;
	}
	public void setFaca(Faca faca) {
		this.faca = faca;
	}
	public ArmaBranca getArmaBranca() {
		return armaBranca;
	}
	public void setArmaBranca(ArmaBranca armaBranca) {
		this.armaBranca = armaBranca;
	}
	public int getNivel() {
		return nivel;
	}
	public void setNivel(int nivel) {
		this.nivel = nivel;
	}
	public String getBase() {
		return base;
	}
	public void setBase(String base) {
		this.base = base;
	}
	
}
